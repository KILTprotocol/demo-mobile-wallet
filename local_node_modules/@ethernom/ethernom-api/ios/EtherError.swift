import Foundation

typealias EtherErrorValue = Int
@objcMembers public class EtherError: NSObject {
    @objc public static let ETH_SUCCESS: Int = 0
    @objc public static let ETH_FAIL: Int = -1
    @objc public static let ETH_CARD_AUTH_FAIL: Int = -2

    @objc public static let ETH_DISCOVERY_COMPLETE = 0x1001
    @objc public static let ETH_SCAN_FOREVER       = 0x1002
    
    @objc public static let ETH_ADAPTER_FOUND      = 0x1003
    @objc public static let ETH_ADAPTER_NOT_FOUND  = 0x1004

    // card errors
    public static let ERR_ETH_CARDSCAN_FAILED = 0x0101
    
    // Select errors
    public static let ERR_ETH_SELECT_DENIED = 0x0102
    public static let ERR_ETH_SELECT_FAILED = 0x0103
    public static let ERR_ETH_DISCONNECTED = 0x0104
    
    // service errors
    public static let ERR_ETH_SERVICE_OPEN_DENIED = 0x0201
}
