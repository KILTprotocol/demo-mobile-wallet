import Foundation

@objcMembers public class CardInfo : NSObject{
  public var _cardName: String
  public var _deviceID: String

  public init(name: String, manufacturingID: String) {
    self._cardName = name
    self._deviceID = manufacturingID
  }

  public func GetFullName()->String{
    return self._deviceID + self._cardName
  }
  
  public func GetCardName() -> String {
    return self._cardName
  }

  public func GetDeviceID() -> String {
    return self._deviceID
  }
}
