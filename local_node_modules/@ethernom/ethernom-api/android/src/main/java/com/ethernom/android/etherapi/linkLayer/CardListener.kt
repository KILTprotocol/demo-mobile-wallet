package com.ethernom.android.etherapi.linkLayer

interface CardListener {
    fun onCardOpenSuccess(resultCode: Int)
    fun onCardOpenFail(resultCode: Int, hwSpecificError: Int)

    fun onCardClosedSucess(resultCode: Int)
    fun onCardClosedByCard(resultCode: Int)

    fun onWriteToCardSuccess(resultCode: Int)
    fun onWriteToCardFail(resultCode: Int, hwSpecificError: Int)
    fun onCardConnectionDropped(resultCode: Int)
    fun onReadFromCardSuccess(resultCode: Int, buffer:ByteArray)
    fun onReadFromCardFail(resultCode: Int, hwSpecificError: Int)
}