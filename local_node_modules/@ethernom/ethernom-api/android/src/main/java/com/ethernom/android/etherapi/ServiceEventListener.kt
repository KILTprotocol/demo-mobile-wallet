package com.ethernom.android.etherapi

abstract class ServiceEventListener {
    abstract fun onWriteSuccess(resultCode: Int)
    abstract fun onWriteFail(resultCode: Int, hwSpecificError: Int)

    abstract fun onServiceClosedSucess(resultCode: Int)
    abstract fun onServiceClosedFail(resultCode: Int, hwSpecificError: Int)
    abstract fun onServiceClosedByService(resultCode: Int)
}