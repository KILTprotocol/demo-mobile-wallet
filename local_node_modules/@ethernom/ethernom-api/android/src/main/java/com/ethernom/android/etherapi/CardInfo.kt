package com.ethernom.android.etherapi

data class CardInfo(
        var _cardName: String,
        var _deviceID: String,
        var _sn : String
) {

    public fun init(name: String, manufacturingID: String, sn:String) {
        _cardName = name
        _deviceID = manufacturingID
        _sn = sn
    }

    public fun GetFullName():String{
        return _deviceID + _cardName
    }

    public fun GetCardName() : String {
        return _cardName
    }

    fun GetDeviceID(): String {
        return _deviceID
    }
    fun GetSN(): String { return _sn;}
}